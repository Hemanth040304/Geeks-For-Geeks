class Solution {
  public:
    bool canSplit(vector<int>& arr) {
        // code here
        int n = arr.size(), sum;
        for (int i = 1; i < n; i++) {
            arr[i] += arr[i - 1];
        }
        
        sum = arr[n-1];
        for (int i = 0; i < n; i++) {
            if (2 * arr[i] == sum) return true;
        }
        return false;
    }
};


