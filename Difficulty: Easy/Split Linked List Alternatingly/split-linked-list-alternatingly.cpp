class Solution {
  public:
    // Function to split a linked list into two lists alternately
    vector<Node*> alternatingSplitList(struct Node* head) {
        vector<Node*> ans(2, nullptr);
        if (!head or !head -> next) return ans;
        
        Node *first = head, *second = head -> next;
        ans[0] = first;
        ans[1] = second;
        
        head = head -> next -> next;
    
        while (head) {
            first -> next = head;
            first = first -> next;
            head = head -> next;
    
            if (head) {
                second -> next = head;
                second = second -> next;
                head = head -> next;
            }
        }
    
        first -> next = nullptr;
        second -> next = nullptr;
    
        return ans;
    }  

};


