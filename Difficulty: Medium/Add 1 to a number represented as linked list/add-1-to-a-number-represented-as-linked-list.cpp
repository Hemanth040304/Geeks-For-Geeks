class Solution {
private:
    int add(Node *head) {
        if (head == nullptr) return 1;
        
        int sum = head -> data + add(head -> next);
        head -> data = sum % 10;
    
        return sum / 10;
    }
    
public:
    Node* addOne(Node *head) {
        int carry = add(head);
        
        if (carry == 1) {
            Node* newHead = new Node(1);
            newHead -> next = head;
            return newHead;
        }
        
        return head;
    }
};

